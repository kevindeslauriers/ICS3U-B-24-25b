package day1;

public class ExampleThree {
    public static void main(String[] args) {
        int numStudents = 14;       // variables use camelCase

        double volume = 123.7;      // double holds floating point decimal numbers

        boolean largeClass = numStudents > 20;  // boolean allows for values of true and false

        System.out.println(numStudents);
        System.out.println(volume);
        System.out.println(largeClass);

        int a = 5, b = 7;
        int sum = a + b;

        System.out.println(sum);

        double c = 6.5, d = 10;

        System.out.println(d);
        System.out.println(c + d);

        System.out.println(c/d);
        System.out.println(a/b);
        System.out.println(b/a);

        System.out.println(7%5);      // 7 / 5 has a remainder of 2 
                                      //  % is the remainder

        System.out.println(7/5);        // int division and is 1 5 goes in to 7 1 time




    }
}
